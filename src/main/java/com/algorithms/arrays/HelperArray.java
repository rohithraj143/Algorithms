package com.algorithms.arrays;

public class HelperArray {

    public static void printArray(int[] arr) {
        for(int a : arr) {
            System.out.print(a + " ");
        }
        System.out.println();
    }

    public static void printArray(char[] ch) {
        for(char c : ch) {
            System.out.print(c + " ");
        }
        System.out.println();
    }
}
